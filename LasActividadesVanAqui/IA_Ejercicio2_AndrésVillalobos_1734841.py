#utlizando input introduces los numeros enteros y flotantes
x = input("Introduce primer numero entero: ")
y = input("Introduce segundo numero entero: ")
z = input("Introduce primer numero flotante: ")
w = input("Introduce segundo numero flotante: ")

#Utilizando print y el comando int espesificas que te den numeros enteros
print("Suma Enteros: ")
print(int(x) + int(y))
print("Resta Enteros: ")
print(int(x) - int(y))
print("Multiplicación Enteros: ")
print(int(x) * int(y))
print("Division Enteros: ")
print(int(x) / int(y))

#Utilizando print y el comando float espesificas que te den numeros flotantes
print("Suma Flotante: ")
print(float(z) + float(w))
print("Resta Flotante: ")
print(float(z) - float(w))
print("Multiplicación Flotante: ")
print(float(z) * float(w))
print("Division Flotante: ")
print(float(z) / float(w))
